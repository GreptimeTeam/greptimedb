CREATE TABLE demo(
    host STRING,
    ts TIMESTAMP,
    cpu DOUBLE NULL,
    memory DOUBLE NULL,
    disk_util DOUBLE DEFAULT 9.9,
    TIME INDEX (ts),
    PRIMARY KEY(host)
)
PARTITION BY RANGE COLUMNS (host) (
    PARTITION r0 VALUES LESS THAN ('550-A'),
    PARTITION r1 VALUES LESS THAN ('550-W'),
    PARTITION r2 VALUES LESS THAN (MAXVALUE),
);

-- SQLNESS REPLACE (-+) -
